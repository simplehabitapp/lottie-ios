import Foundation

public struct Color {
  
  public var r: Double
  public var g: Double
  public var b: Double
  public var a: Double
  
  public init(r: Double, g: Double, b: Double, a: Double) {
    self.r = r
    self.g = g
    self.b = b
    self.a = a
  }
  
}
